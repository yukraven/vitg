import unittest
from Resources import Location


class TestRepr(unittest.TestCase):

    def testCorrectInformation(self):
        location = Location.Location("SD", "D", ["C1", "C2"], {"D": 0, "WD": 1, "WT": 1, "T": 1},
                                     {"D": {"0": [100, 0, 0, 0], "1": [100, 0, 0, 0],
                                            "2": [100, 0, 0, 0], "3": [100, 0, 0, 0]},
                                      "TD": {"1": 1, "2": 2, "3": 3, "4": 4},
                                      "WD": {"0": [100, 0, 0, 0, 0, 0, 0], "1": [100, 0, 0, 0, 0, 0, 0]},
                                      "WT": {"0": [100, 0, 0, 0, 0, 0, 0]},
                                      "TM": {"0": [100, 0, 0, 0, 0, 0, 0]},
                                      "TND": 1},
                                     {"D": {"0": ["1", "2", "3", "4"]},
                                      "WD": {"0": "0", "1": "1"},
                                      "WT": {"0": "0", "1": "1"}})
        self.assertEqual(repr(location), "SD | D |  C1 C2 |  0 1 1 1 |  "
                                         "{'0': [100, 0, 0, 0], '1': [100, 0, 0, 0], "
                                         "'2': [100, 0, 0, 0], '3': [100, 0, 0, 0]} "
                                         "{'1': 1, '2': 2, '3': 3, '4': 4} "
                                         "{'0': [100, 0, 0, 0, 0, 0, 0], '1': [100, 0, 0, 0, 0, 0, 0]} "
                                         "{'0': [100, 0, 0, 0, 0, 0, 0]} "
                                         "{'0': [100, 0, 0, 0, 0, 0, 0]} "
                                         "1")
